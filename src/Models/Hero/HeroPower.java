package Models.Hero;

public class HeroPower {

    public String name;
    public String type;
    public String description;
    public int mana;

    public HeroPower(String name, String type, String description, int mana) {
        this.name = name;
        this.type = type;
        this.description = description;
        this.mana = mana;
    }
}
